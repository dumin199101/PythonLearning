#深拷贝与浅拷贝
import copy
a = [11,22,33]
#浅拷贝
b = a
#深拷贝
c = copy.deepcopy(a)
print(id(a),a)
print(id(b),b)
print(id(c),c)
a.append(44)
print("-"*100)
print(id(a),a)
print(id(b),b)
print(id(c),c)

